package ex_02_inc_Operators;

public class Lab07_inc_Operator {
    public static void main(String[] args) {
        int i = 1;
        i = i++;

        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
        int a = 5;
        System.out.println(++a); // A
        System.out.println(a++); // B
        System.out.println(a);   // C

    }
  }
