package ex_02_inc_Operators;

public class Lab08_dec_operator {
    public static void main(String[] args) {
        int b = 5;
        System.out.println(--b); // A
        System.out.println(b--); // B
        System.out.println(b);   // C
        int a=9;
        System.out.println(a--);
        System.out.println(a);

    }
}
